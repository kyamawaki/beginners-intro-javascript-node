let str1 = "Hello";
let str2 = "World";

// use + operator
console.log(str1 + str2);
console.log(str1 + "Big" + str2);

// adding spacing
str1 = "Hello ";
console.log(str1 + str2);
console.log(str1 + "Big " + str2);

// 数値の1が"1"に変換され"1"+"2"⇒"12"となる
let num1 = 1;
let num2 = "2";
console.log(num1 + num2);
console.log(num1 + 1);